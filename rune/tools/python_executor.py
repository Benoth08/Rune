"""Python executor tool — V5.1 sandboxed subprocess runner.

Runs Python code generated by the LLM (or pasted by the user) in
an isolated subprocess with strict limits. Used by the V5.1 tool
router when the user's question involves computation, plotting,
or code execution.

Sandbox model
-------------
We use ``subprocess.run`` instead of ``exec()`` because :
1. A separate process protects the main Lythéa server from RuntimeError
   /memory leaks / infinite loops (we can ``kill -9`` after timeout).
2. A clean ``cwd`` (temp dir) limits filesystem damage scope — code
   can read/write inside its temp dir but not the project root.
3. A pruned ``env`` removes secrets that might be in the main env.

What this does NOT protect against
----------------------------------
- ``os.system("rm -rf $HOME")`` — the subprocess inherits the user's
  filesystem permissions, so it can wipe ``/home/user``. For real
  multi-user / untrusted code, use Docker or nsjail. Acceptable for
  a local single-user setup where you'd type the same commands yourself.
- Network egress — the subprocess can hit the internet. If you want
  network isolation, run Lythéa inside a Docker container with
  ``--network none``.
- Slow exfiltration via DNS — same as above.

For Mika's solo local usage, ``subprocess + timeout + clean cwd``
is the right trade-off. Document the limit ; tighten with Docker
if Lythéa ever serves external users.

API
---
``run(code, timeout=5.0, allow_plots=True)`` → dict :
    {
        "ok": bool,
        "stdout": str,        # captured stdout
        "stderr": str,        # captured stderr
        "result": str,        # last expression value if any
        "duration_ms": int,
        "plots": list[str],   # base64 PNG of any plt.show() calls
        "error": str | None,  # only set if ok=False
    }
"""

