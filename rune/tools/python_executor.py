DEFAULT_TIMEOUT_S = 5.0
MAX_TIMEOUT_S = 30.0
MAX_OUTPUT_BYTES = 64 * 1024  # 64 KB stdout/stderr cap

# Modules autorisés à l'import (en plus de la stdlib). Si la liste est
